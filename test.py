def my_preprocess(image):
    if np.sum(image)/(img_row*img_col*255)<0.005:
        
        image1=image/(np.max(image)+.004)
        for r in range(img_row):
            for c in range(img_col):
                if image1[r,c]>0.1:
                    image1[r,c]=1
        image1=image1*255
    else:
        image1=image
    return image1


